"""Визуальная (гомоглифная) транслитерация для ужатия payload в эфире LoRa.

Принцип: для каждой кириллической буквы — ближайший по НАЧЕРТАНИЮ ASCII-аналог
(того же регистра), а не по звучанию. Это уменьшает payload (кириллица — 2 байта,
ASCII — 1 байт) и оставляет текст узнаваемым «по виду».

Правила подбора (см. docs/superpowers/specs/2026-06-28-visual-transliterate-design.md):
тот же регистр, без цифр, только уверенные совпадения. Буквы без хорошего аналога
(в т.ч. строчные в/н/т — их аналог B/H/T только в другом регистре) остаются
кириллицей. Отсюда регистровая асимметрия: В/Н/Т→B/H/T, но в/н/т без изменений;
п→n, но П остаётся.
"""

from __future__ import annotations

_TABLE = str.maketrans({
    # строчные
    'а': 'a', 'г': 'r', 'е': 'e', 'ё': 'e', 'и': 'u',
    'к': 'k', 'м': 'm', 'о': 'o', 'п': 'n', 'р': 'p',
    'с': 'c', 'у': 'y', 'х': 'x', 'ш': 'w', 'ь': 'b',
    # заглавные (их начертание совпадает с латиницей — регистр менять не нужно)
    'А': 'A', 'В': 'B', 'Е': 'E', 'Ё': 'E', 'Н': 'H',
    'К': 'K', 'М': 'M', 'О': 'O', 'Р': 'P', 'С': 'C',
    'Т': 'T', 'У': 'Y', 'Х': 'X', 'Ш': 'W',
})


def transliterate(text: str) -> str:
    """Заменить кириллицу визуальными ASCII-аналогами; прочее — без изменений."""
    return text.translate(_TABLE)
